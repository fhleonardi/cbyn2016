    <div class="col-xs-12 col-md-8"><h2><img src="{{ asset('img/titulo.png')}}"> La ciudad</h2>

        <p>La ciudad de <strong>Gualeguaychú</strong>, cabecera del departamento Gualeguaychú en la provincia de Entre
            Ríos, se ubica en el sudeste de la provincia, a 230 km de la ciudad de Buenos Aires y a 25 km de la
            República
            Oriental del Uruguay. <br>
            Su nombre es una deformación del término guaraní Yaguarí Guazú: Yaguar: Tigre; I: Río; Guazú: Grande, o sea
            &quot;Río del Tigre Grande&quot;. También se cree que otros indígenas que habitaban la región  le llamaban 
            &quot;Agua
            de andar lento&quot; o &quot;Río de las cuevas de los chanchos  chicos&rdquo;.</p>

        <p>
            Sus principales actividades económicas son el agro, la industria y el turismo. El turismo se desarrolla
            en base a
            su privilegiada geografía y naturaleza con ríos, arroyos, montes y termas -en ellos  tiene lugar la
            recreación
            acuática en sus hermosas playas y deportes náuticos, pesca, camping, así como en el senderismo,
            reconocimiento de
            aves y pequeños animales silvestres-. No obstante, actualmente se ha centrado en el denominado &quot;Carnaval
            del
            País&quot;, que se considera el de mayor trascendencia y calidad en Argentina y el tercero en cantidad de
            espectadores del mundo.</p>

        <p>
            Una vasta tradición cultural e histórica puede apreciarse en su gente, sus edificios, su parque y sus
            museos.  En
            ella nacieron o vivieron artistas,  científicos y escritores. Figuras académicas, pintores, concertistas de
            piano,
            bailarinas, escultores, y escenógrafos.</p>

        <p>
            Los gualeguaychuenses la definen como la &ldquo;Ciudad de los Poetas&rdquo; ya que ha sido pródiga en hombre
            de
            letras a los que honra en el Rincón de los Poetas en su Plaza San Martín. </p>

        <p>
            El clima es templado pampeano, con invierno frío y con heladas –con temperaturas mínimas de -5ºC- y veranos
            calurosos –con temperaturas máximas en promedio de 30ºC-35ªC- </p>

        <p>
            En el mes de octubre, y en base a datos históricos, las temperaturas mínimas oscilan entre 8ºC y 12ºC, y las
            máximas
            entre 20ºC y 26ºC. Pueden presentarse días con vientos leves, alguna nubosidad y también lluvias. La humedad
            relativa promedio anual es de 75%.</p><br>
