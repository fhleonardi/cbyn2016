    <div class="col-xs-12 col-md-8"><h2><img src="{{ asset('img/titulo.png')}}" alt="titulo"> Reglamento para la presentación de trabajos de investigación</h2>

        <p><u>INTRODUCCIÓN </u></p>
        <p>La Facultad de Bromatología de la Universidad Nacional de Entre Ríos convoca a docentes, estudiantes y
            egresados de las carreras de la Facultad de Bromatología y profesionales de carreras relacionadas, a
            compartir sus producciones científicas en el Congreso de Bromatología y Nutrición que se llevará a cabo del
            12 al 14 de octubre de 2016, en el Centro de Convenciones de la Municipalidad de Gualeguaychú. </p>
        <p><u>REGLAMENTO PARA LA PRESENTACIÓN DE TRABAJOS DE INVESTIGACIÓN</u></p>
        <p><u>AREAS TEMÁTICAS</u></p>
        <p>Los trabajos deberán estar comprendidos en algunas de las siguientes áreas:</p>
        <ul>
            <li>Desarrollo de alimentos</li>
            <li>Calidad Alimentaria</li>
            <li>Nutrición</li>
            <li>Bromatología</li>
            <li>Salud y Políticas Públicas</li>
            <li>Soberanía y Seguridad Alimentaria</li>
        </ul>
        <p>&nbsp;</p>
        <p><u>1 - GENERALIDADES</u></p>

        <p>1.1 - Los Trabajos a presentar deben enmarcarse en las áreas previamente definidas.</p>
        <p>1.2 - Cada autor podrá presentar hasta 3 trabajos y deberá inscribirse.</p>

        <p>1.3 - Los trabajos NO podrán ser reportes de casos aislados, revisiones bibliográficas o monografías. </p>
        <p>1.4 - El Comité Científico se reserva el derecho de: a) No aceptar los trabajos que no cumplan los requisitos
            establecidos por este reglamento; b) Incluir los trabajos en el área que el Comité Científico juzgue
            conveniente, informando de manera anticipada a los autores.</p>
        <p>1.5 - Cualquier situación no contemplada en este reglamento será oportunamente examinada y resuelta por el
            Comité Científico.</p>
        <p>1.6 - El Comité Científico se reserva el derecho de modificar parcialmente el reglamento actual, previa
            notificación y aprobación de los cambios introducidos, por la Comisión Organizadora. De ser aprobados los
            cambios proyectados, éstos serán comunicados a la brevedad. </p>
        <p>1.7 - La participación de un trabajo de Investigación en forma de <em><u>Resumen</u></em>  en el Congreso se
            hace efectiva mediante la exposición pública en formato de póster u oral.</p>
        <p>&nbsp;</p>
        <p><u>2 - NORMAS PARA LA PRESENTACION DE RESÚMENES</u></p>

        <p>2.1 - Para enviar un trabajo al Congreso de Bromatología y Nutrición, el autor deberá preinscribirse para
            obtener una clave personal que le permitirá acceder al formulario &ldquo;Envío de Resúmenes&rdquo;,
            disponible en la pestaña ´Presentación de Trabajos´ de la página web del Congreso
            (cbyn.fb.uner.edu.ar/congreso).<br>
            Al ingresar al formulario deberá seleccionar una de las áreas temáticas en la que se encuadre su trabajo y
            su preferencia en la forma de presentación (oral o como póster)<br>
            Si existen otros autores, también ellos deberán pre-inscribirse aunque no tendrán que registrar nuevamente
            el trabajo</p>
        <p>Se sugiere nombrar los archivos con este criterio:</p>
        <p><em>Apellido_iniciales nombre_area tematica</em></p>
        <p>2.2 - El resumen del Trabajo de Investigación deberá presentarse con el tipo de letra Arial tamaño 12, estilo
            normal e interlineado 1,5, margen de 3 cm. en todos sus lados, justificado y en hoja A4.</p>
        <p>2.3 - <u>Título:</u> centrado, en letra Arial tamaño 12, minúsculas, excepto la primera letra y en negrita.
        </p>
        <p>2.4 - <u>Autores:</u> separado de la información anterior por 1,5 líneas, centrado, letra Arial 12; apellido
            con minúsculas, excepto la primera letra y luego la inicial del nombre. El relator deberá estar subrayado.
        </p>
        <p>2.5 - <u>Lugar de trabajo:</u> separado de la información anterior por 1,5 líneas,  centrado, letra Arial 10,
            mencionar la institución a la que pertenecen los autores y la dirección electrónica solamente del relator,
            sin incluir direcciones postales ni cargos. </p>
        <p>Ejemplo: <br>
            <u>Rodríguez C.E.L.</u> (1), Pérez D. (2), Moreira T. (1)</p>
        <p>(1) Universidad Nacional de Tucumán. Tucumán, Argentina.<br>
            (2) Universidad Nacional de Entre Ríos. Entre Ríos, Argentina.<br>
            celrodriguez@gmail.com</p>

        <p>2.6 - Texto del resumen: separado de la información anterior con un doble espacio, en letra Arial tamaño 12,
            interlineado sencillo y texto justificado. Deberá incluir los siguientes apartados: <em>objetivos,
                materiales y métodos, resultados relevantes y conclusiones</em>. Contendrá hasta 500 palabras. </p>
        <p>2.7 - Palabras claves: separadas de la información anterior por 1,5 líneas, deberán ser entre 3 (tres) como
            mínimo y 5 (cinco) como máximo.</p>
        <p>2.8 - Abreviaturas: mencionar el término completo la 1ra vez seguido de la abreviatura (entre paréntesis) que
            se utilizará en el resto del texto. </p>
        <p>2.9 - Fecha límite para el envío de los resúmenes: <strong>30 de septiembre de 2016</strong></p>
        <p>Los mismos deberán ser enviados electrónicamente en formato Word mediante la web del congreso.- </p>
        <p> Fecha de notificación de aceptación de los Resúmenes: el Comité Científico comunicará al primer firmante, la
            aceptación del resumen y la forma de presentación.</p>
        <p>2.10 - Todos los resúmenes aceptados serán publicados en el Libro de Resúmenes.</p>
        <p>&nbsp;</p>
        <p><u>3 - DE LOS PLAZOS Y PRESENTACIONES: </u></p>


        <ul>
            <li><strong>Fecha límite para el envío de Resúmenes: 30 de septiembre de 2016</strong></li>
            <li><strong>Fecha de notificación de la aceptación de Resúmenes: 4 de octubre, se confirmará vía e-mail la
                    modalidad de presentación del trabajo (oral o póster).</strong></li>
            <li><strong>En caso de presentación oral, el Comité Organizador informará el día y  horario en que debe
                    hacerse la presentación.</strong></li>
            <li><strong>Los autores deberán respetar las Normas para la Presentación de Resúmenes de Trabajos de
                    Investigación establecido por el Comité Científico. </strong></li>
            <li><strong>La participación de un Trabajo de Investigación en Congreso de Bromatología y Nutrición se hará
                    efectiva al ser expuesto públicamente en forma oral o póster.</strong></li>
        </ul>
        <p>&nbsp;</p>
        <p><u>4 - NORMAS PARA LA PRESENTACION ORAL DE RESÚMENES </u></p>
        <p>La presentación oral se realizará en la fecha y horario le que sea notificado al autor que hubiera registrado
            el trabajo en la página del Congreso.</p>
        <p>4.1 - La presentación oral no deberá superar los 15 minutos.</p>
        <p>4.2 - El autor podrá utilizar diapositivas en formato PowerPoint o Prezi, o póster, el autor deberá
            corroborar la compatibilidad del archivo con el dispositivo a utilizar. </p>

        <p>&nbsp;</p>
        <p><u>5 - NORMAS PARA LA PRESENTACION DE PÓSTERS DE RESÚMENES</u></p>
        <p>5.1 - Cada Poster debe ser presentado en la plantilla respetando el formato, el color del fondo y los campos
            solicitados, quedando a criterio del autor/es el tamaño y los colores de relleno de estos últimos. Las
            plantillas están a disposición en la pestaña ´Presentación de Trabajos´ de la página web del Congreso.</p>
        <p>5.2 - La presentación tiene que ser enviada en formato PowerPoint 2003 - 2007 - 2010 con extensión .ppt, en
            una sola placa, con orientación horizontal, sin videos ni animaciones. </p>
        <p>5.3 - Las fotografías deberán estar en formato jpg, no así las tablas y gráficos, que serán presentados en el
            formato original. </p>
        <p>5.4 - El tamaño máximo aceptado es de 2MB. </p>
        <p>5.5 - Se especifican como variable de fuentes: Verdana, Times New Roman y Arial de tamaño 10 a 12.</p>
        <p>&nbsp;</p>
        <p>Todos los trabajos seleccionados serán finalmente aprobados en tanto y en cuanto el responsable del mismo se
            encuentre inscripto al evento.-</p>
        <p>Lugar designado para la presentación de posters: Centro de Convenciones – Cosmódromo de Gualeguaychú, acceso
            lado sur sobre calle Estrada.</p>
        <p>5.6 - La modalidad de presentación será unicamente mediante póster electrónico (e-poster)</p>
