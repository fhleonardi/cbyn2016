    <div class="col-xs-12 col-md-8"><h2><img src="{{ asset('img/titulo.png')}}" alt="titulo"> Como llegar</h2>

<p>La Ruta Prov. Nº 20, conecta a la ciudad con el Centro y Noroeste de la Provincia de Entre Ríos.</p>

<p>La Ruta Nac. Nº 14 es la vía de comunicación con Buenos Aires y con el Norte de la Mesopotamia. Esta autovía se recorre desde Ceibas en Entre Ríos hasta Bernardo de Irigoyen en la provincia de Misiones.</p>

<p>La Ruta Prov. N° 16, que lo vincula con el Oeste de la provincia, por las Rutas Prov. Nº 11 y Nº12,  y la ciudad de Rosario (Santa Fe) a través del puente Rosario-Victoria y la Ruta Nac. Nº 174.</p>

<p>La Ruta Nac. Nº 136 es la vía que conecta a la República Oriental del Uruguay con Argentina a través del paso fronterizo Fray Bentos- Puerto Unzué.</p>


<strong>Gualeguaychú cuenta con los siguientes accesos:</strong>
<ul><li>
Acceso Norte:
Prolongación de Ruta Prov. N° 20.  Transitando desde la R.P. 20 o desde la RN 14 circulando de Norte a Sur.
</li></ul>

<ul><li>
Acceso por puente Internacional Gral. San Martín:
Desde la R.O.U. a través del puente internacional, que la vincula con la ciudad uruguaya de Fray Bentos, circulando por la Ruta Nac. 136
</li></ul>

<ul><li>
Acceso Sur:
Prolongación de Ruta Prov. N° 16.  Transitando desde la Ruta Prov.  Nº16 o desde la Ruta Nac.  14 circulando de Sur a Norte.
</li></ul>

<ul><li>
Acceso Oeste:
Desde Ruta Nac. 14 por calle Urquiza. Esta calle atraviesa la ciudad, inicia en la costanera del Río Gualeguaychú y se prolonga más allá de la Ruta Nac. Nº 14
</li></ul>


<strong>Peajes</strong>
<li>Zárate Brazo Largo (Ruta 12)</li>
<li>Túnel Subfluvial (Paraná - Santa Fé)</li>
<li>Puente Int. Lib. Gral. San Martín (R.O.U.)</li>
<li>Puente Victoria - Rosario</li>
<li>Colonia Elia (Ruta 14 – Localidad situada al Norte de Gchú.)</li><br>
