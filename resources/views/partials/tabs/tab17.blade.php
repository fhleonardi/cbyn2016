        <blockquote>“El descubrimiento de un nuevo plato hace más por la felicidad de la humanidad que el descubrimiento
            de una nueva estrella”. <footer>Jean Anthelme Brillat-Savarin</footer>
        </blockquote>
        <p>La gastronomía es una actividad que en los últimos años se ha intensificado y expandido generando un gran
            impacto social y económico a nivel global. Entre las razones que argumentan en este sentido, están la mejor
            formación y capacitación que están logrando los profesionales del sector.</p>
        <p>
            En la búsqueda de promover cambios tangibles en la calidad de vida de nuestra comunidad, entre otros
            objetivos, el <em>Workshop</em> “Nuevas herramientas para la vieja cocina” se presenta como una estrategia
            que pretende ser motor y parte de una articulación entre organismos públicos y privados, brindar
            capacitación, acercar recursos y liderar un espacio de diálogo e intercambio para potenciar las iniciativas
            y capacidades de la propia comunidad local y regional.</p>
        <p>
            Reconocidos profesionales de España y Argentina, han sido convocados para el <em>Workshop</em> que se
            llevará a cabo el viernes 14 de octubre, a partir de las 9:00, en el Centro de Convenciones Gualeguaychú. La
            doctora Purificación García Segovia, de la Universidad Politécnica de Valencia, Mariana Koppman y Marcelo
            Kulish, de la Asociación de Gastronomía Molecular Argentina, realizarán una puesta en escena de platos y
            preparaciones. Los participantes podrán apreciar <em>en vivo</em> algunas de las transformaciones posibles
            en las texturas de ingredientes, así como su forma y temperatura.</p>
        <p>
            La gastronomía está en evolución constantemente y en este proceso hay una etapa en la que comenzó a
            interactuar más estrechamente con la ciencia. Así, en el mundo de sensaciones de la cocina pueden convivir
            un científico y un cocinero dotando al universo de sabores de un nuevo sentido estético.</p>


        <p>El <em>Workshop</em> se desarrollará el día viernes durante toda la jornada bajo los siguientes ejes: </p>

        <p>• Panorámica de la gastronomía <br>
            • Del falso caviar al trampantojo<br>
            • Cocción a baja temperatura <br>
            • Espumas – Reacción del comensal.<br>
        </p><br>
